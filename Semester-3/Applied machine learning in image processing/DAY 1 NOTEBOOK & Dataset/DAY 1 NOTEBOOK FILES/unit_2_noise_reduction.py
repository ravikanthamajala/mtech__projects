def add_speckle_noise(img):
    noise = np.random.randn(*img.shape[:2])[:, :, None]
    noisy = img/255 + (img/255)*noise
    noisy = np.clip(noisy, 0, 1)
    return (noisy * 255).astype(np.uint8)

