def add_poisson_noise(image):
    # Normalize image to [0,1]
    img_norm = image / 255.0

    # Calculate unique values to scale noise properly
    vals = len(np.unique(img_norm))
    vals = 2 ** np.ceil(np.log2(vals))  # next power of 2

    # Apply Poisson noise
    noisy = np.random.poisson(img_norm * vals) / float(vals)

    # Clip to valid range and convert back
    noisy = np.clip(noisy, 0, 1)
    noisy = (noisy * 255).astype(np.uint8)

    return noisy

